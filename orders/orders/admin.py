"""
Кастомная конфигурация административной панели Django.

Определяет настраиваемый класс CustomAdminSite для изменения заголовков
и внешнего вида стандартной административной панели Django.

Используется для:
- Изменения заголовков и названий в админ-панели
- Создания экземпляра кастомизированной админ-панели
- Централизованной настройки административного интерфейса
"""

from django.contrib.admin import AdminSite

class CustomAdminSite(AdminSite):
    """
    Кастомный класс административной панели Django.
    
    Наследуется от стандартного AdminSite и переопределяет
    метаданные для изменения внешнего вида админ-панели.
    
    Attributes:
        site_header (str): Заголовок в верхней части панели администратора
        site_title (str): Заголовок вкладки браузера
        index_title (str): Заголовок на главной странице администратора
    """
    # Заголовок, отображаемый в верхней части каждой страницы администратора
    site_header = "Панель управления заказами"

    # Текст, отображаемый в заголовке вкладки браузера
    site_title = "Админка заказов"

    # Заголовок на главной странице администратора (над списком приложений)
    index_title = "Управление данными"

# Создание экземпляра кастомной административной панели
# Этот объект может быть использован для регистрации моделей вместо admin.site
admin_site = CustomAdminSite(name='custom_admin')